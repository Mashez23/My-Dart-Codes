void main() {
  // Integer (int) - for whole numbers
  int age = 23;
  print("My age is $age"); // String interpolation for output

  // Double - for decimal numbers
  double pi = 3.14159;
  print("The value of pi is $pi");

  // String - for text data
  String name = "Brian Muhanji";
  print("My name is $name");

  // List - ordered collection of items
  List<String> colors = ["red", "green", "blue"];
  print("Primary colors: $colors");

  // Map - collection of key-value pairs
  Map<String, String> fruits = {"apple": "red", "banana": "yellow"};
  print("Fruit colors: $fruits");
}
